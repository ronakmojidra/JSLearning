console.log(`----------------Implicit String conversion to number---------------`);
let result;
console.log(`1> (result = '4' - '2';)-----------`);
result = '4' - '2';
console.log(`The result is: ${result}`);
// console.log(typeof result);
console.log(`Both '4' and '2' are string and it's implicitly converted in to number`);
console.log(`2> (result = '4' - 2;)-----------`);
result = '4' - 2;
console.log(`The result is: ${result}`);
console.log(`'4' which is a string implicitly converted in to number`);
console.log(`3> (result = '4' * 2;)-----------`);
result = '4' * 2;
console.log(`The result is: ${result}`);
